package com.xiaojiaoshi.exception;

public class XiaoJiaoShiException extends RuntimeException
{

	public XiaoJiaoShiException()
	{
		super();
	}

	public XiaoJiaoShiException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XiaoJiaoShiException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public XiaoJiaoShiException(String message)
	{
		super(message);
	}

	public XiaoJiaoShiException(Throwable cause)
	{
		super(cause);
	}

	
	

}
